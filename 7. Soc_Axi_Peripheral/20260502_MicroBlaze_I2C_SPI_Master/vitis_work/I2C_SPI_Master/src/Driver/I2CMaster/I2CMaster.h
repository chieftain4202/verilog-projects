#ifndef SRC_DRIVER_I2CMASTER_I2CMASTER_H_
#define SRC_DRIVER_I2CMASTER_I2CMASTER_H_

#include "xil_types.h"

#define I2C_MASTER_REG0_OFFSET 0U
#define I2C_MASTER_REG1_OFFSET 4U
#define I2C_MASTER_REG2_OFFSET 8U
#define I2C_MASTER_REG3_OFFSET 12U
#define I2C_MASTER_STATUS_OFFSET I2C_MASTER_REG1_OFFSET
#define I2C_MASTER_STATUS_STATE_MASK 0x0000000FU
#define I2C_MASTER_STATUS_BUSY_MASK 0x00000010U
#define I2C_MASTER_STATUS_DONE_MASK 0x00000020U
#define I2C_MASTER_STATUS_ACK_MASK 0x00000040U
#define I2C_MASTER_STATUS_START_DB_MASK 0x00000080U
#define I2C_MASTER_STATUS_START_SEEN_MASK 0x00000100U
#define I2C_MASTER_STATUS_SW_TX_SHIFT 9U
#define I2C_MASTER_STATUS_M_TX_SHIFT 17U
#define I2C_MASTER_STATUS_TX_MASK 0x000000FFU

typedef struct {
	u32 baseAddress;
} I2CMaster_t;

void I2CMaster_Init(I2CMaster_t *hi2c, u32 baseAddress);
void I2CMaster_WriteSwWord(I2CMaster_t *hi2c, u16 swWord);
u32 I2CMaster_ReadStatus(const I2CMaster_t *hi2c);
u32 I2CMaster_ReadReg(const I2CMaster_t *hi2c, u32 regOffset);

#endif /* SRC_DRIVER_I2CMASTER_I2CMASTER_H_ */
